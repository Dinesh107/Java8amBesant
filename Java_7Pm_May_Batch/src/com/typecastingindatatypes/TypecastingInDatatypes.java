package com.typecastingindatatypes;

public class TypecastingInDatatypes {

	public static void main(String[] args) {
		
		// Typecasting In Datatypes
		
//		 short a = 100;
//		 
//		 byte b = 20;
//		 
//		 a = b;
//		 
//		 // Narrowing Typecasting ---> Converting Lower datatype
//		 
//		 // into higher datatype
//		 
//		 System.out.println(a);
		
		
		byte a = 100;
		
		short b = 200; 
		
		a = (byte) b;
		
		// Widening Typecasting ---> Converting Higher Datatype 
		// Lower Datatype
		
		System.out.println(a);
		

	}

}
